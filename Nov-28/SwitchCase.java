
public class SwitchCase {
    public static void main(String[] args) {
        // syntax
        int number = 2;
        switch (number) {
            case 0:
                System.out.println("zero");
                break;
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("tow");
                break;
            default:
                break;
        }

    }
}
